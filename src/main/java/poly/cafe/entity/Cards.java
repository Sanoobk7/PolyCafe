package poly.cafe.entity;

/**
 *
 * @author Home
 */
public class Cards {
    private int id;
    private int status;

    public Cards() {
    }

    public Cards(int id, int status) {
        this.id = id;
        this.status = status;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }
}
